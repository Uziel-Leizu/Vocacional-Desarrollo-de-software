package appperro;


public class Perro {
    private String nombre;
    private int numero;

    public Perro(String nombre, int numero) {
        this.nombre = nombre;
        this.numero = numero;
    }

    public String getNombre() {
        return nombre;
    }

    public int getNumero() {
        return numero;
    }
    
    
    
    
}
